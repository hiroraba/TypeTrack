import Foundation

enum Categories: String {
    case meigen = "名言"
    case life = "生活"
    case science = "科学"
    case history = "歴史"
    case technology = "技術"
    case culture = "文化"
    case art = "芸術"
    case news = "ニュース"
    
    var chatGPTPrompt: String {
        switch self {
        case .meigen:
            return """
                偉人や著名人の実在する名言や発言を基にした、タイピング練習用の日本語課題文を作成してください。
                名言はそのままでも、背景や補足を含めて少し拡張してもかまいません。
                共通ルールに従ってください。
                """
        case .life:
            return """
                日常生活に役立つ豆知識・生活ハック・健康法・節約術などの実在情報を基に、日本語のタイピング練習文を作成してください。
                共通ルールに従い、英単語・記号・改行を適宜含めてください。
                """
        case .science:
            return """
            実在する自然科学の原理・現象・生物・数値・法則などを説明する日本語のタイピング課題文を1つ作成してください。
            「重力」「DNA」「光速」「酸と塩基」「π=3.14」など、学習にもなるテーマにしてください。共通ルールに従ってください。
            """
        case .history:
            return """
                実際に起きた日本または世界の歴史的な出来事や人物に基づいた内容で、知識にもなる日本語の課題文を作成してください。
                共通ルールに従い、英単語や記号（例：“Revolution”, “BC 300”など）を自然に含めてください。
                """
        case .technology:
            return """
            実在する技術・テクノロジー・発明・ガジェットなどを紹介するタイピング用の日本語文を作成してください。
            たとえば「AI」「インターネット」「5G」「スマートフォン」などがテーマになります。共通ルールに従ってください。
            """
        case .culture:
            return """
                実在する日本または世界の文化・習慣・行事を紹介するタイピング練習文を、日本語で1つ作成してください。
                例えば「茶道」「Halloween」「祭り」などをテーマにし、共通ルールを守ってください。
                """
        case .art:
            return """
                実在する芸術作品・ジャンル・アーティストについて紹介するタイピング練習文を日本語で1つ作成してください。
                音楽、絵画、建築、映画など幅広く取り上げて構いません。共通ルールを守ってください。
                """
        case .news:
            return """
                最近の国際的な出来事やトピック（例：“COP26”, 世界的な環境問題 など）をもとに、日本語のタイピング課題文を作成してください。
                英語や記号も含め、読んで理解が深まる内容にしてください。※事実に基づき、誇張や憶測は避けてください。
                """
        }
    }
}
