import Foundation

public enum TypingCategory: String, CaseIterable {
    case meigen = "名言"
    case life = "生活"
    case science = "科学"
    case history = "歴史"
    case technology = "技術"
    case culture = "文化"
    case art = "芸術"
    case news = "ニュース"
    case phirosphy = "哲学"
}

public extension TypingCategory {
    var chatGPTPrompt: String {
        switch self {
        case .meigen:
            return """
                偉人や著名人の実在する名言や発言を基にした、タイピング練習用の日本語課題文を作成してください。
                同じ人物や言葉に偏らず、さまざまな時代や地域の名言を取り上げてください。
                名言はそのままでも、背景や補足を含めて少し拡張してもかまいません。
                共通ルールに従ってください。
                """
        case .life:
            return """
                日常生活に役立つ豆知識・生活ハック・健康法・節約術などの実在情報を基に、日本語のタイピング練習文を作成してください。
                毎回同じテーマに偏らないよう、バランスよくさまざまな話題を取り上げてください。
                共通ルールに従い、英単語・記号・改行を適宜含めてください。
                """
        case .science:
            return """
            実在する自然科学の原理・現象・生物・数値・法則などを説明する日本語のタイピング課題文を1つ作成してください。
            毎回同じテーマに偏らないよう、物理・化学・生物・地学・天文学など幅広い分野をバランスよく扱ってください。
            英単語（例：“gravity”, “atom”, “orbit”）や記号（例：“π=3.14”, “H2O”, “→”）を1つ以上含め、知識にもなるよう構成してください。
            あくまで事実に基づき、共通ルールには従ってください。
            """
        case .history:
            return """
            実際に起きた日本または世界の歴史的な出来事や人物に基づいた内容で、知識にもなる日本語の課題文を作成してください。
            毎回同じテーマに偏らないよう、さまざまな歴史的出来事や人物を取り上げてください。
            共通ルールに従い、英単語や記号（例：“Revolution”, “BC 300”など）を自然に含めてください。
            """
        case .technology:
            return """
            実在する技術・テクノロジー・発明・ガジェットなどを紹介するタイピング用の日本語文を作成してください。
            毎回同じテーマに偏らないよう、バランスよくさまざまな技術や発明を取り上げてください。
            たとえば「AI」「インターネット」「5G」「スマートフォン」などがテーマになります。あくまで一例なのでそれ以外のテーマも大歓迎です。ただ共通ルールには従ってください。
            """
        case .culture:
            return """
            実在する日本または世界の文化・習慣・行事を紹介するタイピング練習文を、日本語で1つ作成してください。
            毎回同じテーマに偏らないよう、バランスよくさまざまな文化や習慣を取り上げてください。
            例えば「茶道」「Halloween」「祭り」などがテーマです。あくまで一例なのでそれ以外のテーマも大歓迎です。ただ共通ルールには従ってください。
            """
        case .art:
            return """
            実在する芸術作品・ジャンル・アーティストについて紹介するタイピング練習文を日本語で1つ作成してください。
            毎回同じテーマに偏らないよう、バランスよくさまざまな芸術作品やアーティストを取り上げてください。
            音楽、絵画、建築、映画など幅広く取り上げて構いません。共通ルールを守ってください。
            """
        case .news:
            return """
            最近の国際的な出来事やトピック（例：“COP26”, 世界的な環境問題 など）をもとに、日本語のタイピング課題文を作成してください。
            毎回同じテーマに偏らないよう、バランスよくさまざまなニュースを取り上げてください。
            英語や記号も含め、読んで理解が深まる内容にしてください。※事実に基づき、誇張や憶測は避けてください。共通ルールに従ってください。
            """
        case .phirosphy:
            return """
            実在する哲学者や哲学の概念・思想・名言などに基づいた、日本語のタイピング課題文を1つ作成してください。
            毎回同じテーマに偏らないよう、バランスよくさまざまな哲学者や思想を取り上げてください。
            プラトン、アリストテレスに限らず、東洋哲学（例：孔子、空海）、近代以降の哲学（例：デカルト、カント、サルトル、ハンナ・アーレント）なども積極的に含めてください。
            文中には英単語（例：“ethics”, “existence”）や記号（例：“?” や “→”）を1つ以上含め、知識にもなるように構成してください。
            内容は実際に存在する哲学者の思想や用語、問いかけに基づいたものに限ってください。
            """
        }
    }
}
